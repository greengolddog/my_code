#include <bits/stdc++.h>
typedef uint_fast32_t ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n;
	cin >> n;
	vector<ll> arr(n, 0);
	ll sum = 0;
	for (ll i = 0; i < n; i++) {
		cin >> arr[i];
		sum += arr[i];
	}
	if (sum % 2 == 1) {
		cout << 0;
		return 0;
	}
	vector<bool> ans(sum + 1, true);
	bitset<250'001> can;
	bitset<250'001> save;
	can[0] = 1;
	save = can;
	for (auto i : arr) {
		can |= can << i;
	}
	if (!can[sum / 2]) {
		cout << 0;
		return 0;
	}
	for (ll i = 0; i < n; i++) {
		can = save;
		if (i != 0) {
			can |= can << arr[i - 1];
		}
		save = can;
		for (ll j = i + 1; j < n; j++) {
			can |= can << arr[j];
		}
		for (ll k = 0; k <= sum; k++) {
			ll bt = sum + k - arr[i];
			if (bt % 2 == 1) {
				ans[k] = false;
				continue;
			}
			bt /= 2;
			if (bt < k || !can[bt - k]) {
				ans[k] = false;
			}
		}
	}
	vector<ll> a;
	for (ll i = 0; i <= sum; i++) {
		if (ans[i]) {
			a.push_back(i);
		}
	}
	cout << a.size() << '\n';
	for (auto i : a) {
		cout << i << ' ';
	}
}
