template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n;
	cin >> n;
	ll ans = 0;
	for (ll i = 1; i <= n; i++) {
		if ((n / i) * (n / i) > n) {
			ans += n / i * i;
		} else {
			break;
		}
	}
	for (ll i = 1; i * i <= n; i++) {
		ans += i * ((n / i) - (n / (i + 1))) * (n / i + n / (i + 1) + 1) / 2;
	}
	cout << ans;
}
