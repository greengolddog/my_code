#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

mt19937 rnd(time(0));

struct bit {
	vector<ll> tree;
	ll size;
	bit(ll n) {
		size = n;
		tree.resize(n + 1, 0);
	}
	ll get(ll x) {
		ll ans = 0;
		for (; x > 0; x -= x & -x) {
			ans += tree[x];
		}
		return ans;
	}
	ll get(ll l, ll r) {
		return get(r) - get(l);
	}
	void change(ll x, ll add) {
		x++;
		for (; x <= size; x += x & -x) {
			tree[x] += add;
		}
	}
};

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n;
	cin >> n;
	bit up(n), down(n);
	vector<pair<ll, ll>> arr(n);
	for (ll i = 0; i < n; i++) {
		cin >> arr[i].first;
		arr[i].second = i;
	}
	sort(arr.begin(), arr.end());
	ll ans = 1;
	bool b = true;
	for (; ans < n; ans++) {
		ll l = arr[ans].second, r = arr[ans - 1].second;
		if (r < l) {
			swap(l, r);
		}
		if (b) {
			if (up.get(l, r) != 0) {
				break;
			} else {
				ll now = rnd();
				up.change(l, now);
				up.change(r, -now);
			}
		} else {
			if (down.get(l, r) != 0) {
				break;
			} else {
				ll now = rnd();
				down.change(l, now);
				down.change(r, -now);
			}
		}
		b = !b;
	}
	cout << ans;
}
