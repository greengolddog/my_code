#include <bits/stdc++.h>
typedef int ll;

const ll INF = 1'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n;
	cin >> n;
	vector<ll> arr[5000];
	for (ll i = 0; i < n; i++) {
		ll a, b;
		cin >> a >> b;
		a--;
		b--;
		arr[a].push_back(b);
		arr[b].push_back(a);
	}
	ll md = 0, col = 0;
	ll dist[5000];
	queue<ll> q;
	ll var[5000];
	for (ll i = 0; i < n; i++) {
		for (ll i = 0; i < n; i++) {
			dist[i] = INF;
		}
		dist[i] = 0;
		for (ll i = 0; i < n; i++) {
			var[i] = 0;
		}
		var[i] = 1;
		q.push(i);
		while (!q.empty()) {
			ll now = q.front();
			q.pop();
			for (auto i : arr[now]) {
				if (dist[i] == dist[now] + 1) {
					col += (md == dist[i]) * var[now];
					var[i]++;
				}
				if (dist[i] > dist[now] + 1) {
					if (assign_max(md, dist[now] + 1)) {
						col = 0;
					}
					dist[i] = dist[now] + 1;
					var[i] += var[now];
					col += (md == dist[i]) * var[now];
					q.push(i);
				}
			}
		}
	}
	cout << col / 2;
}
