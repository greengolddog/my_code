#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n, m;
	cin >> n >> m;
	vector<ll> arr(n, 0);
	for (ll i = 0; i < n; i++) {
		cin >> arr[i];
	}
	set<ll> all;
	vector<ll> where(n + 1, -1);
	where[0] = 1;
	all.insert(0);
	for (auto i : arr) {
		auto it = all.lower_bound(i);
		it--;
		cout << where[*it] << ' ';
		where[i] = where[*it];
		if (*it != 0) {
			all.erase(it);
		} else {
			where[*it]++;
		}
		all.insert(i);
	}
	cout << '\n';
	for (ll i = 1; i <= n; i++) {
		cout << where[i] << ' ';
	}
}
