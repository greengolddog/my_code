#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

ll solve(ll l, ll r) {
	ll ans = 0;
	while (r >= l) {
		ans += (r / 2) - ((l - 1) / 2);
		r = r / 2;
		l = (l + 1) / 2;
	}
	if (r == l) {
		ans += 1 - r % 2;
	}
	return ans;
}

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll l, r;
	cin >> l >> r;
	cout << solve(l, r);
}
