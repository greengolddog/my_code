typedef int ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n, q;
	cin >> n >> q;
	vector<ll> arr(n, 0);
	for (ll i = 0; i < n; i++) {
		cin >> arr[i];
	}
	for (ll i = 0; i < q; i++) {
		string s;
		cin >> s;
		if (s == "xor") {
			ll l, r, x;
			cin >> l >> r >> x;
			l--;
			for (ll i = l; i < r; i++) {
				arr[i] ^= x;
			}
		} else {
			ll l, r;
			cin >> l >> r;
			l--;
			vector<ll> dp(8, 0);
			for (ll i = l; i < r; i++) {
				assign_max(dp[arr[i]], (ll)1);
				for (ll j = 0; j < arr[i]; j++) {
					assign_max(dp[arr[i]], dp[j] + 1);
				}
			}
			ll ans = 0;
			for (auto i : dp) {
				assign_max(ans, i);
			}
			cout << ans << '\n';
		}
	}
}
