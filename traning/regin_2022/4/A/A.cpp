#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	string s;
	cin >> s;
	vector<ll> prefs(1, 0), prefu(1, 0);
	for (auto i : s) {
		prefs.push_back(prefs.back() + (i == 'S'));
		prefu.push_back(prefu.back() + (i == 'U'));
	}
	ll ans = 0;
	for (ll i = 0; i <= (s.size() - 3); i++) {
		ll l = i + 3, r = s.size() + 1;
		while (r - l > 1) {
			ll mid = (l + r) / 2;
			if (prefs[mid] - prefs[i] <= 1 || prefu[mid] - prefu[i] <= 1) {
				l = mid;
			} else {
				r = mid;
			}
		}
		ll l2 = i + 2, r2 = s.size() + 1;
		while (r2 - l2 > 1) {
			ll mid = (l2 + r2) / 2;
			if (prefs[mid] - prefs[i] == 0 || prefu[mid] - prefu[i] == 0) {
				l2 = mid;
			} else {
				r2 = mid;
			}
		}
		if (prefs[s.size()] - prefs[i] == 0 || prefu[s.size()] - prefu[i] == 0) {
			continue;
		}
		ans += l - l2;
	}
	cout << ans;
}
