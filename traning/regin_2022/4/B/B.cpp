#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n, k;
	cin >> n >> k;
	multiset<ll> up, down;
	for (ll i = 0; i < n; i++) {
		ll a;
		cin >> a;
		if (a >= 0) {
			up.insert(-a);
		} else {
			down.insert(a);
		}
	}
	for (ll i = 0; i < k; i++) {
		ll x;
		cin >> x;
		if (x == 1) {
			if (down.size() > 0) {
				up.insert(*down.begin());
				down.erase(down.begin());
			}
		} else {
			if (up.size() > 0) {
				down.insert(*up.begin());
				up.erase(up.begin());
			}
		}
	}
	ll ans = 0;
	for (auto i : down) {
		ans += i;
	}
	for (auto i : up) {
		ans -= i;
	}
	cout << ans;
}
