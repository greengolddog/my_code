#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n;
	cin >> n;
	vector<vector<ll>> can_be_son(n);
	for (ll i = 0; i < n; i++) {
		ll k;
		cin >> k;
		for (ll j = 0; j < k; j++) {
			ll a;
			cin >> a;
			a--;
			can_be_son[a].push_back(i);
		}
	}
	ll ans = INF;
	for (ll i = 0; i < n; i++) {
		vector<ll> dist(n, INF);
		dist[i] = 0;
		queue<ll> q;
		q.push(i);
		while (!q.empty()) {
			ll x = q.front();
			q.pop();
			for (auto i : can_be_son[x]) {
				if (assign_min(dist[i], dist[x] + 1)) {
					q.push(i);
				}
			}
		}
		ll now = n;
		for (auto i : dist) {
			if (i == INF) {
				now = INF;
				break;
			}
			now += i;
		}
		assign_min(ans, now);
	}
	cout << ans;
}
