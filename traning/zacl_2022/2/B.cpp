#include <bits/stdc++.h>
typedef int_fast32_t ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n, m;
	cin >> n >> m;
	map<pair<ll, ll>, bool> used;
	vector<vector<ll>> arr(n, vector<ll>(m));
	set<ll> s;
	for (ll i = 0; i < n; i++) {
		for (ll j = 0; j < m; j++) {
			cin >> arr[i][j];
			s.insert(arr[i][j]);
		}
	}
	ll ans = 1;
	if (s.size() <= 2) {
		cout << n * m;
		return 0;
	}
	vector<pair<ll, ll>> go(1, make_pair(0, 1));
	go.push_back(make_pair(1, 0));
	go.push_back(make_pair(-1, 0));
	go.push_back(make_pair(0, -1));
	for (ll i = 0; i < n; i++) {
		for (ll j = 0; j < m; j++) {
			for (auto[x, y] : go) {
				if (!used[make_pair(i * m + j, (i + x) * m + j + y)]) {
					if (i + x >= 0 && i + x < n && j + y >= 0 && j + y < m) {
						ll ni = i + x, nj = j + y;
						ll c1 = arr[i][j], c2 = arr[ni][nj];
						if (c1 == c2) {
							continue;
						}
						//used[make_pair(i * m + j, ni * m + nj)] = true;
						//used[make_pair(ni * m + nj, i * m + j)] = true;
						queue<pair<ll, ll>> q;
						q.push(make_pair(i, j));
						q.push(make_pair(ni, nj));
						map<pair<ll, ll>, bool> u;
						u[make_pair(i, j)] = true;
						u[make_pair(ni, nj)] = true;
						ll now = 2;
						while (!q.empty()) {
							auto[x, y] = q.front();
							q.pop();
							for (auto[ax, ay] : go) {
								if (x + ax >= 0 && x + ax < n && y + ay >= 0 && y + ay < m) {
									if (!u[make_pair(x + ax, y + ay)] && !used[make_pair(x * m + y, x * m + y + ax * m + ay)]) {
										if (arr[x + ax][y + ay] == c1 || arr[x + ax][y + ay] == c2) {
											now++;
											u[make_pair(x + ax, y + ay)] = true;
											//used[make_pair(x * m + y, x * m + y + ax * m + ay)] = true;
											//used[make_pair(x * m + y + ax * m + ay, x * m + y)] = true;
											q.push(make_pair(x + ax, y + ay));
										}
									}
								}
							}
						}
						assign_max(ans, now);
					}
				}
			}
		}
	}
	cout << ans;
}
