#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n, k;
	cin >> n >> k;
	ll da = 1, ca = 0;
	if (n > k * 2) {
		da = n - k;
		ca = 1;
	}
	for (ll i = 2; i <= 80'000'000; i++) {
		ll x = n;
		ll now = 0;
		while (x > 0 && (x % i == k)) {
			now++;
			x /= i;
		}
		if (assign_max(ca, now)) {
			da = i;
		}
	}
	cout << da << ' ' << ca;
}
