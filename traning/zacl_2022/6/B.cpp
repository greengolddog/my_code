#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

bool dfs(ll v, ll p, ll f, vector<bool>& used, vector<vector<ll>>& arr) {
	used[v] = true;
	for (auto i : arr[v]) {
		if (i != p && i != f) {
			if (used[i]) {
				return true;
			}
			if (dfs(i, v, f, used, arr)) {
				return true;
			}
		}
	}
	return false;
}

void dfs2(ll v, ll p, ll f, vector<ll>& dist, vector<vector<ll>>& arr) {
	dist[v] = dist[p] + 1;
	for (auto i : arr[v]) {
		if (i != p && i != f) {
			dfs2(i, v, f, dist, arr);
		}
	}
}

void solve() {
	ll n, m, s, t;
	cin >> n >> m >> s >> t;
	s--;
	t--;
	vector<vector<ll>> arr(n);
	for (ll i = 0; i < m; i++) {
		ll a, b;
		cin >> a >> b;
		a--;
		b--;
		arr[a].push_back(b);
		arr[b].push_back(a);
	}
	vector<bool> used(n, false);
	if (dfs(s, s, t, used, arr)) {
		cout << -1 << '\n';
		return;
	}
	vector<ll> dist(n, 0);
	dfs2(s, s, t, dist, arr);
	ll ans = 0;
	for (auto i : arr[t]) {
		assign_max(ans, dist[i]);
	}
	cout << ans << '\n';
}

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll t;
	cin >> t;
	for (ll _ = 0; _ < t; _++) {
		solve();
	}
}
