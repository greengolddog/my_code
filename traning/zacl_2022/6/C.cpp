#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll x, y;
	cin >> x >> y;
	ll k, n;
	cin >> n >> k;
	vector<tuple<ll, ll, ll, ll>> arr(n);
	for (auto&[a, b, c, d] : arr) {
		cin >> a >> b >> c >> d;
	}
	ll ax1 = 0, ax2 = 0, ay1 = 0, ay2 = 0;
	for (ll i = 1; i < (1 << n); i++) {
		ll mx = x, my = y, nx = 0, ny = 0;
		ll col = 0, sum = 0;
		for (ll j = 0; j < n; j++) {
			if ((i >> j) & 1) {
				col++;
				auto[x1, y1, x2, y2] = arr[j];
				assign_min(mx, x1);
				assign_min(my, y1);
				assign_min(mx, x2);
				assign_min(my, y2);
				assign_max(nx, x1);
				assign_max(ny, y1);
				assign_max(nx, x2);
				assign_max(ny, y2);
				sum += abs(x1 - x2) * abs(y1 - y2);
			}
		}
		if (col > k) {
			continue;
		}
		if (sum != (nx - mx) * (ny - my)) {
			continue;
		}
		if ((ax2 - ax1) * (ay2 - ay1) < sum) {
			ax1 = mx;
			ax2 = nx;
			ay1 = my;
			ay2 = ny;
		}
	}
	cout << ax1 << ' ' << ay1 << ' ' << ax2 << ' ' << ay2;
}
