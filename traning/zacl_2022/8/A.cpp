#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n;
	cin >> n;
	vector<ll> a(n), b(n);
	for (ll i = 0; i < n; i++) {
		cin >> a[i];
		cin >> b[i];
	}
	vector<ll> dpav(n, INF), dpbv(n, INF);
	dpav[0] = 0;
	dpbv[0] = 1;
	for (ll i = 1; i < n; i++) {
		if (a[i] < a[i - 1]) {
			assign_min(dpav[i], dpav[i - 1]);
		}
		if (a[i] < b[i - 1]) {
			assign_min(dpav[i], dpbv[i - 1]);
		}
		if (b[i] < a[i - 1]) {
			assign_min(dpbv[i], dpav[i - 1] + 1);
		}
		if (b[i] < b[i - 1]) {
			assign_min(dpbv[i], dpbv[i - 1]);
		}
	}
	vector<ll> dpau(n, INF), dpbu(n, INF);
	dpau[0] = 0;
	dpbu[0] = 1;
	for (ll i = 1; i < n; i++) {
		if (a[i] > a[i - 1]) {
			assign_min(dpau[i], dpau[i - 1]);
		}
		if (a[i] > b[i - 1]) {
			assign_min(dpau[i], dpbu[i - 1]);
		}
		if (b[i] > a[i - 1]) {
			assign_min(dpbu[i], dpau[i - 1] + 1);
		}
		if (b[i] > b[i - 1]) {
			assign_min(dpbu[i], dpbu[i - 1]);
		}
	}
	if (dpav.back() == INF && dpbv.back() == INF && dpau.back() == INF && dpbu.back() == INF) {
		cout << -1;
		return 0;
	}
	cout << min(min(dpav.back(), dpbv.back()), min(dpau.back(), dpbu.back()));
}
