#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n;
	cin >> n;
	vector<ll> arr(n, 0);
	for (ll i = 0; i < n; i++) {
		cin >> arr[i];
	}
	ll s;
	cin >> s;
	ll ans = INF, ex = 0, nu = 0;
	for (ll i = 0; i < s; i++) {
		ll kl, kr, ml, mr;
		cin >> kl >> kr >> ml >> mr;
		for (ll j = 0; j < (1 << n); j++) {
			ll now = 0;
			vector<ll> fir, sec;
			for (ll k = 0; k < n; k++) {
				if ((j >> k) & 1) {
					fir.push_back(arr[k]);
				} else {
					sec.push_back(arr[k]);
				}
				if (k > 0) {
					if (((j >> (k - 1)) & 1) == ((j >> k) & 1)) {
						now++;
					}
				}
			}
			bool b = true;
			for (ll k = 1; k < (ll)fir.size(); k++) {
				b = b && (fir[k] - fir[k - 1] >= kl) && (fir[k] - fir[k - 1] <= kr);
			}
			for (ll k = 1; k < (ll)sec.size(); k++) {
				b = b && (sec[k] - sec[k - 1] >= ml) && (sec[k] - sec[k - 1] <= mr);
			}
			if (b && assign_min(ans, now)) {
				nu = i;
				ex = j;
			}
		}
	}
	if (ans == INF) {
		cout << 0;
		return 0;
	}
	cout << nu + 1 << ' ' << ans << '\n';
	for (ll i = 0; i < n; i++) {
		cout << (((ex >> i) & 1) ? 1 : 2);
	}
}
