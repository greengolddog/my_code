#include <bits/stdc++.h>
typedef long long ll;

using namespace std;

const ll INF = 1'000'000'000'000'000;

int main() {
	freopen("inexact-matching.in", "r", stdin);
	freopen("inexact-matching.out", "w", stdout);
	ios_base::sync_with_stdio(false);
	cin.tie(0);
	cout.tie(0);
	string s, t;
	cin >> s >> t;
	s.push_back('@');
	s.push_back('&');
	ll ss = s.size() - 2;
	s += t;
	ll n = s.size();
	deque<ll> arr(n + 1, 0);
	arr[0] = -1;
	for (ll i = 2; i <= n; i++) {
		ll now = arr[i - 1];
		while (now != -1 && s[now] != s[i - 1]) {
			now = arr[now];
		}
		arr[i] = now + 1;
	}
	arr.pop_front();
	vector<ll> ans(n, 0);
	for (ll i = 1; i < n; i++) {
		ans[i] = arr[i - 1] + 1;
		if (s[i] == s[ans[i - 1]]) {
			ans[i] = max(ans[i], ans[i - 1] + 1);
		}
	}
	vector<ll> a;
	for (ll i = ss + 2; i < n; i++) {
		if (ans[i] >= ss) {
			a.push_back(i - ss * 2);
		}
	}
	cout << a.size() << '\n';
	for (auto i : a) {
		cout << i << ' ';
	}
}

