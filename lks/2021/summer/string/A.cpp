#include <bits/stdc++.h>
typedef long long ll;

using namespace std;

const ll INF = 1'000'000'000'000'000;

int main() {
	freopen("prefix-function.in", "r", stdin);
	freopen("prefix-function.out", "w", stdout);
	ios_base::sync_with_stdio(false);
	cin.tie(0);
	cout.tie(0);
	string s;
	cin >> s;
	ll n = s.size();
	vector<ll> arr(n + 1, 0);
	arr[0] = -1;
	for (ll i = 2; i <= n; i++) {
		ll now = arr[i - 1];
		while (now != -1 && s[now] != s[i - 1]) {
			now = arr[now];
		}
		arr[i] = now + 1;
	}
	for (ll i = 1; i <= n; i++) {
		cout << arr[i] << ' ';
	}
}

