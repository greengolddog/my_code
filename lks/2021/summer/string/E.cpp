#include <bits/stdc++.h>
typedef long long ll;

using namespace std;

const ll INF = 1'000'000'000'000'000;

int main() {
	freopen("period.in", "r", stdin);
	freopen("period.out", "w", stdout);
	ios_base::sync_with_stdio(false);
	cin.tie(0);
	cout.tie(0);
	string s;
	cin >> s;
	ll n = s.size();
	vector<ll> arr(n, 0);
	arr[0] = n;
	ll start = -1, end = -1;
	for (ll i = 1; i < n; i++) {
		if (end <= i) {
			start = i;
			end = i;
			while (end < n && s[arr[i]] == s[end]) {
				end++;
				arr[i]++;
			}
		} else {
			arr[i] = min(end - i, arr[i - start]);
			if (i + arr[i] == end) {
				start = i;
				while (end < n && s[arr[i]] == s[end]) {
					end++;
					arr[i]++;
				}
			}
		}
	}
	ll i = 1;
	for (; i < n; i++) {
		if (n % i == 0) {
			bool b = true;
			for (ll j = 0; j < n; j += i) {
				if (arr[j] < i) {
					b = false;
					break;
				}
			}
			if (b) {
				break;
			}
		}
	}
	cout << i;
}

