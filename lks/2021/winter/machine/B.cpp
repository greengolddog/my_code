#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
	freopen("bulbul.in", "r", stdin);
	freopen("bulbul.out", "w", stdout);
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	string s;
	cin >> s;
	ll n, m, k;
	cin >> n >> m >> k;
	vector<bool> arr(n, false);
	for (ll i = 0; i < k; i++) {
		ll x;
		cin >> x;
		x--;
		arr[x] = true;
	}
	map<pair<ll, char>, ll> go;
	for (ll i = 0; i < m; i++) {
		ll a, b;
		char c;
		cin >> a >> b >> c;
		a--;
		b--;
		go[make_pair(a, c)] = b;
	}
	ll now = 0;
	for (auto i : s) {
		if (go.find(make_pair(now, i)) == go.end()) {
			cout << "Rejects";
			return 0;
		}
		now = go[make_pair(now, i)];
	}
	cout << (arr[now] ? "Accepts" : "Rejects");
}
