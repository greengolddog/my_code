#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
	freopen("comments.in", "r", stdin);
	freopen("comments.out", "w", stdout);
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	string s;
	bool col = false;
	ll lt = -1;
	while (getline(cin, s)) {
		string na;
		char l = 'a';
		s.push_back('\n');
		for (auto i : s) {
			if (i == '/' && l == '/' && !col) {
				na.pop_back();
				na.push_back('\n');
				break;
			}
			if (i == '{' && lt == 0) {
				return 1;
			}
			if (i == '{' && !col) {
				col = true;
				lt = 0;
				l = 'a';
				continue;
			}
			if (i == '*' && l == '/' && lt == 1) {
				return 1;
			}
			if (i == '*' && l == '/' && !col) {
				na.pop_back();
				col = true;
				lt = 1;
				l = 'a';
				continue;
			}
			if (!col) {
				na.push_back(i);
			}
			if (i == '}' && lt == 0 && col) {
				col = false;
				lt = -1;
				l = 'a';
				continue;
			}
			if (i == '/' && l == '*' && lt == 1 && col) {
				col = false;
				lt = -1;
				l = 'a';
				continue;
			}
			l = i;
		}
		if (!na.empty()) {
			cout << na;
		}
	}
}
