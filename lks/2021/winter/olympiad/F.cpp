#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	string n;
	cin >> n;
	vector<ll> col(10, 0);
	for (auto i : n) {
		col[i - '0']++;
	}
	ll k;
	cin >> k;
	for (ll i = 0; i < k; i++) {
		vector<ll> nc(10, 0);
		for (ll j = 0; j < 10; j++) {
			for (auto l : to_string(j * j * j)) {
				nc[l - '0'] += col[j];
			}
		}
		col = nc;
	}
	ll sum = 0;
	for (auto i : col) {
		sum += i;
	}
	cout << sum;
}
