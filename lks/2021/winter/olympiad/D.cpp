#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n, m;
	cin >> n >> m;
	vector<vector<ll>> arr(n);
	for (ll i = 0; i < n; i++) {
		ll x;
		cin >> x;
		arr[i].resize(x);
		for (ll j = 0; j < x; j++) {
			cin >> arr[i][j];
			arr[i][j]--;
		}
	}
	vector<ll> a(m);
	for (ll i = 0; i < m; i++) {
		cin >> a[i];
		a[i]--;
	}
	ll ans = m * 2;
	for (ll i = 0; i < n; i++) {
		bool b = true;
		for (auto& j : arr[i]) {
			j = a[j];
			if (b && j == i) {
				ans--;
				ans--;
			} else {
				b = false;
			}
		}
	}
	cout << ans;
}
