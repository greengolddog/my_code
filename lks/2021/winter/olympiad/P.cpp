int main() {
	freopen("towels.in", "r", stdin);
	freopen("towels.out", "w", stdout);
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n, m;
	cin >> n >> m;
	vector<pair<ll, ll>> arr(n);
	for (ll i = 0; i < n; i++) {
		cin >> arr[i].first;
		arr[i].second = i;
	}
	sort(arr.begin(), arr.end());
	reverse(arr.begin(), arr.end());
	vector<ll> ans;
	for (auto[i, n] : arr) {
		if (i < m) {
			m = (i + m + 1) / 2;
			ans.push_back(n);
		}
	}
	cout << ans.size() << '\n';
	for (auto i : ans) {
		cout << i + 1 << ' ';
	}
}
