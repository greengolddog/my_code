#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

void solve() {
	ll n;
	cin >> n;
	ll ans = 0;
	for (ll i = 0; i < n; i++) {
		ll x;
		cin >> x;
		if (x > 2) {
			if (x % 4 == 0) {
				x--;
			} else {
				if (x % 4 == 3) {
					x++;
				}
			}
		}
		ans ^= x;
	}
	cout << (ans != 0 ? "FIRST" : "SECOND") << '\n';
}

int main() {
	freopen("varnim.in", "r", stdin);
	freopen("varnim.out", "w", stdout);
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll t;
	cin >> t;
	for (ll _ = 0; _ < t; _++) {
		solve();
	}
}
