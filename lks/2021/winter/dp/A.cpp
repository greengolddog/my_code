#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

struct matrix {
	vector<vector<ll>> arr;
	matrix(vector<vector<ll>> arr): arr(arr) {
	}
	matrix(ll n = 0) {
		arr.resize(n, vector<ll>(n, 0));
		for (ll i = 0; i < n; i++) {
			arr[i][i] = 1;
		}
	}
	matrix operator* (matrix m) {
		matrix ans(vector<vector<ll>>(arr.size(), vector<ll>(m.arr[0].size(), 0)));
		for (ll i = 0; i < arr.size(); i++) {
			for (ll j = 0; j < m.arr[0].size(); j++) {
				for (ll k = 0; k < m.arr.size(); k++) {
					ans.arr[i][j] += arr[i][k] * m.arr[k][j];
				}
			}
		}
		return ans;
	}
};

ostream& operator<< (ostream &out, matrix m) {
	for (auto i : m.arr) {
		for (auto j : i) {
			cout << j << ' ';
		}
		cout << '\n';
	}
	return out;
}

int main() {
	freopen("mmul.in", "r", stdin);
	freopen("mmul.out", "w", stdout);
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll p, q, r;
	cin >> p >> q >> r;
	vector<vector<ll>> m1(p, vector<ll>(q)), m2(q, vector<ll>(r));
	for (ll i = 0; i < p; i++) {
		for (ll j = 0; j < q; j++) {
			cin >> m1[i][j];
		}
	}
	for (ll i = 0; i < q; i++) {
		for (ll j = 0; j < r; j++) {
			cin >> m2[i][j];
		}
	}
	cout << matrix(m1) * matrix(m2);
}
