#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll m;
	cin >> m;
	vector<tuple<ll, ll, ll>> arr((1 << m) * m + 1);
	cout << arr.size() << '\n' << 1 << ' ' << 1 << ' ' << 1 << '\n';
	for (ll i = 0; i < (1 << m); i++) {
		for (ll j = 0; j < m; j++) {
			if ((i >> (m - 1)) & 1) {
				arr[i * m + j] = make_tuple((1 << m) * m, (1 << m) * m, ((i << 1) & ((1 << m) - 1)) * m + ((j + 1) % m));
				continue;
			}
			get<2>(arr[i * m + j]) = (1 << m) * m;
			get<0>(arr[i * m + j]) = (((i << 1) | 1) & ((1 << m) - 1)) * m + ((j + 1) % m);
			if (j == m - 1 || (i >> (m - 2)) & 1) {
				get<1>(arr[i * m + j]) = (1 << m) * m;
			} else {
				get<1>(arr[i * m + j]) = (((i << 1) | (1 << (m - 1))) & ((1 << m) - 1)) * m + ((j + 1) % m);
			}
		}
	}
	arr.back() = make_tuple((1 << m) * m, (1 << m) * m, (1 << m) * m);
	for (auto[a, b, c] : arr) {
	       cout << a + 1 << ' ' << b + 1 << ' ' << c + 1 << '\n';
      	}	       
}
