typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
	freopen("unfair.in", "r", stdin);
	freopen("unfair.out", "w", stdout);
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n;
	cin >> n;
	ll c = 10'000'000;
	vector<ll> arr(c + 1, -1);
	vector<bool> be(c + 1, true);
	ll cnt = 1;
	for (ll i = 2; i <= c; i++) {
		if (be[i]) {
			arr[i] = cnt;
			cnt++;
			for (ll j = i * 2; j <= c; j += i) {
				be[j] = false;
			}
		}
	}
	ll ans = 0;
	for (ll i = 2; i * i <= n; i++) {
		while (n % i == 0) {
			ans ^= arr[i];
			n /= i;
		}
	}
	if (n > c) {
		cout << "David";
		return 0;
	}
	if (n > 1) {
		ans ^= arr[n];
	}
	cout << (ans == 0 ? "Vasya" : "David");
}
