#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

ll binpow(ll x, ll a, ll mod) {
	if (a == 0) {
		return 1;
	}
	if (a % 2 == 1) {
		return binpow(x, a - 1, mod) * x % mod;
	} else {
		ll b = binpow(x, a / 2, mod);
		return b * b % mod;
	}
}

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n;
	cin >> n;
	ll mod = 1'000'000'007;
	vector<ll> arr(n, 0);
	for (ll i = 0; i < n; i++) {
		cin >> arr[i];
	}
	ll ans = 1;
	for (ll i = 2; i <= n; i++) {
		ll m = INF;
		for (ll j = 0; j < i; j++) {
			ll now = 0;
			for (ll k = j; k < n; k += i) {
				now += arr[k];
			}
			assign_min(m, now);
		}
		ans = ans * binpow(i, m, mod) % mod;
	}
	cout << ans;
}
