	vector<string> arr(h * w);
	for (ll i = 0; i < h * w; i++) {
		cin >> arr[i];
	}
	shuffle(arr.begin(), arr.end(), rnd);
	for (ll i = 0; i < h; i++) {
		for (ll j = 0; j < w; j++) {
			cout << arr[i * w + j] << ' ';
		}
		cout << endl;
	}
