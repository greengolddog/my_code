template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll d, k, l;
	cin >> d >> k >> l;
	if (l <= d) {
		cout << d - l;
		return 0;
	}
	cout << min(l - d - ((l - d) / k) * k, ((l - d) / k + 1) * k - (l - d));
}
