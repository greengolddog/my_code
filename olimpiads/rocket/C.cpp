#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9000000000000000000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n, x;
	cin >> n >> x;
	vector<ll> arr(n, 0);
	for (ll i = 0; i < n; i++) {
		cin >> arr[i];
	}
	if (x == 0) {
		ll cz = 0;
		for (auto i : arr) {
			cz += i == 0;
		}
		ll nz = n - cz;
		if (cz >= nz) {
			cout << n % 2;
		} else {
			cout << nz - cz;
		}
		return 0;
	}
	sort(arr.begin(), arr.end());
	multiset<ll> now;
	for (auto i : arr) {
		if (i % x == 0 && now.find(i / x) != now.end()) {
			now.erase(now.find(i / x));
		} else {
			now.insert(i);
		}
	}
	cout << now.size();
}
