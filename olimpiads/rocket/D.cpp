#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9000000000000000000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll mood;
	cin >> mood;
	if (mood == 0) {
		ll n;
		cin >> n;
		vector<ll> arr(n, 0);
		set<ll> all;
		for (ll i = 0; i < n; i++) {
			cin >> arr[i];
			all.insert(arr[i]);
		}
		if (all.size() == 1) {
			ll ans = 0;
			for (ll i = 0; i < n; i++) {
				ans += (n - i) / 2;
			}
			cout << ans;
			return 0;
		}
		ll ans = 0;
		for (ll i = 0; i < n; i++) {
			set<ll> s;
			for (ll j = i; j < n; j++) {
				if (s.find(arr[j]) != s.end()) {
					s.erase(s.find(arr[j]));
				} else {
					s.insert(arr[j]);
				}
				ans += s.size() == 0;
			}
		}
		cout << ans;
		return 0;
	}
}
