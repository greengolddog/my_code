#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000, mod = 1'000'000'007;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

void dfs(ll v, ll p, vector<vector<ll>>& arr, vector<ll>& h) {
	h[v] = h[p] + 1;
	for (auto i : arr[v]) {
		if (i != p) {
			dfs(i, v, arr, h);
		}
	}
}

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n;
	cin >> n;
	vector<vector<ll>> arr(n);
	for (ll i = 1; i < n; i++) {
		ll x;
		cin >> x;
		x--;
		arr[i].push_back(x);
		arr[x].push_back(i);
	}
	vector<ll> h(n, -1);
	dfs(0, 0, arr, h);
	ll sum = 0, col = 0;
	for (ll i = 0; i < n; i++) {
		ll x;
		cin >> x;
		if (x >= 0) {
			col++;
			sum += x;
			sum %= mod;
		}
	}
	vector<ll> f(1, 1);
	for (ll i = 1; i <= n; i++) {
		f.push_back(f.back() * i % mod);
	}
	vector<ll> h_to_col(n, 0);
	ll now = 1;
	for (ll i = 0; i < n; i++) {
		h_to_col[i] = now * sum % mod * f[n - i - 1] % mod;
		now = now * (col - i - 1) % mod;
	}
	ll ans = 0;
	for (auto i : h) {
		ans += h_to_col[i];
		ans %= mod;
	}
	cout << ans;
}
