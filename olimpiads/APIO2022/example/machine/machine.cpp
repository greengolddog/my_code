template<>
struct std::hash<pair<ll, ll>> {
	ll operator() (pair<ll, ll> p) {
		return ((__int128)p.first * MOD + p.second) % INF64;
	}
};

vector<int> guess_permutation(int n) {
	vector<int> arr(n, 0);
	ll x = use_machine(arr)[0];
	for (ll i = 0; i < n; i++) {
		arr[i] = i;
	}
	vector<int> ans = use_machine(arr);
	for (ll i = 0; i < n; i++) {
		ans[i] ^= x;
	}
	return ans;
}
