#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

struct node {
	node *prev = nullptr, *next = nullptr;
	ll x;
	node(ll x): x(x) {}
};

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n;
	cin >> n;
	node *start = nullptr, *mid = nullptr, *end = nullptr;
	ll col = 0;
	for (ll i = 0; i < n; i++) {
		char c;
		cin >> c;
		if (c == '-') {
			cout << start->x << '\n';
			start = start->prev;
			if (col % 2 == 0) {
				mid = mid->prev;
			}
			col--;
		}
		if (c == '+') {
			ll x;
			cin >> x;
			node *now = new node(x);
			if (col == 0) {
				start = now;
				end = now;
				mid = now;
				col = 1;
				continue;
			}
			end->prev = now;
			now->next = end;
			end = now;
			if (col % 2 == 0) {
				mid = mid->prev;
			}
			col++;
		}
		if (c == '*') {
			ll x;
			cin >> x;
			node *now = new node(x);
			if (col == 0) {
				start = now;
				end = now;
				mid = now;
				col = 1;
				continue;
			}
			if (col == 1) {
				end->prev = now;
				now->next = end;
				end = now;
				col = 2;
				continue;
			}
			mid->prev->next = now;
			now->prev = mid->prev;
			mid->prev = now;
			now->next = mid;
			if (col % 2 == 0) {
				mid = now;
			}
			col++;
		}
	}
}
