#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n, k;
	cin >> n >> k;
	vector<ll> arr(n, 0);
	for (ll i = 0; i < n; i++) {
		cin >> arr[i];
	}
	sort(arr.begin(), arr.end());
	ll ans = arr.back() - arr[0] + 1;
	vector<ll> r;
	for (ll i = 1; i < n; i++) {
		r.push_back(arr[i] - arr[i - 1] - 1);
	}
	sort(r.begin(), r.end());
	k--;
	while (k > 0) {
		ans -= r.back();
		r.pop_back();
		k--;
	}
	cout << ans;
}
