#pragma GCC optimize("Ofast")
#include <bits/stdc++.h>
typedef int_fast64_t ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n;
	cin >> n;
	vector<ll> arr(n, 0);
	for (ll i = 0; i < n; i++) {
		cin >> arr[i];
	}
	vector<vector<ll>> dps(n, vector<ll>((n + 1) / 2 + 1, -INF)), dpsn(n, vector<ll>((n + 1) / 2 + 1, -INF));
	dps[0][1] = arr[0];
	dpsn[0][0] = 0;
	for (ll i = 1; i < n; i++) {
		for (ll j = 0; j < (n + 1) / 2; j++) {
			dpsn[i][j] = max(dps[i - 1][j], dpsn[i - 1][j]);
			dps[i][j + 1] = dpsn[i - 1][j] + arr[i];
		}
		dpsn[i][(n + 1) / 2] = dps[i - 1][(n + 1) / 2];
	}
	for (ll i = 1; i < dps[0].size(); i++) {
		cout << max(dps[n - 1][i], dpsn[n - 1][i]) << '\n';
	}
}
