#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n, m;
	cin >> n >> m;
	vector<vector<ll>> arr(n), costs(n);
	for (ll i = 0; i < m; i++) {
		ll a, b, c;
		cin >> a >> b >> c;
		a--;
		b--;
		arr[b].push_back(a);
		costs[a].push_back(c);
	}
	for (ll i = 0; i < n; i++) {
		sort(costs[i].begin(), costs[i].end());
	}
	vector<ll> dist(n, INF);
	vector<bool> used(n, false);
	dist.back() = 0;
	set<pair<ll, ll>> se;
	for (ll i = 0; i < n; i++) {
		se.insert(make_pair(dist[i], i));
	}
	while (!se.empty()) {
		auto[d, i] = *se.begin();
		se.erase(se.begin());
		used[i] = true;
		for (auto j : arr[i]) {
			ll old = dist[j];
			if (assign_min(dist[j], d + costs[j].back()) && !used[j]) {
				se.erase(se.find(make_pair(old, j)));
				se.insert(make_pair(dist[j], j));
			}
			costs[j].pop_back();
		}
	}
	cout << dist[0];
}
