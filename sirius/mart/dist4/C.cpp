#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n, m;
	cin >> n >> m;
	multiset<ll> mul;
	vector<ll> up(n, 0);
	for (ll i = 0; i < m; i++) {
		ll a, b, c;
		cin >> a >> b >> c;
		up[b] = c;
		mul.insert(c);
	}
	ll q;
	cin >> q;
	for (ll i = 0; i < q; i++) {
		ll a, b;
		cin >> a >> b;
		if (n <= 3) {
			cout << -1 << '\n';
			continue;
		}
		if (a == 0) {
			mul.erase(mul.find(up[b]));
			auto it = mul.begin();
			it++;
			cout << max(up[b], max(*it, *mul.begin()));
			cout << '\n';
			mul.insert(up[b]);
			continue;
		}
		mul.erase(mul.find(up[a]));
		mul.erase(mul.find(up[b]));
		cout << max(*mul.begin(), max(up[a], up[b])) << '\n';
		mul.insert(up[a]);
		mul.insert(up[b]);
	}
}
