template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

void solve() {
	ll n;
	cin >> n;
	for (ll i = 0; i < n; i++) {
		ll a;
		cin >> a;
		cout << a << ' ';
	}
	cout << '\n';
}

void solve2() {
	ll n;
	cin >> n;
	vector<ll> ans(n, 0), arr(n, 0);
	for (ll i = 0; i < n; i++) {
		cin >> arr[i];
		arr[i]--;
		ans[i] = i;
	}
	ll x = 1;
	for (ll i = 1; i <= n; i++) {
		x *= i;
	}
	for (ll i = 1; i < x; i++) {
		vector<ll> new_ans(n, 0);
		for (ll j = 0; j < n; j++) {
			new_ans[arr[j]] = ans[j];
		}
		ans = new_ans;
	}
	for (auto i : ans) {
		cout << i + 1 << ' ';
	}
	cout << '\n';
}

