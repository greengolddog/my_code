#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

void solve() {
	ll l, r;
	cin >> l >> r;
	ll log = 63;
	bool b = false;
	ll ans = 0;
	for (ll i = log; i >= 0; i--) {
		if (((l >> i) & 1) != ((r >> i) & 1)) {
			b = true;
		}
		if (b) {
			ans += (1ll << i);
		}
	}
	cout << ans << '\n';
}

void solveTL() {
	ll l, r;
	cin >> l >> r;
	ll ans = 0;
	for (ll i = l; i <= r; i++) {
		for (ll j = l; j <= r; j++) {
			assign_max(ans, i ^ j);
		}
	}
	cout << ans << '\n';
}

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll t;
	cin >> t;
	for (ll i = 0; i < t; i++) {
		solve();
	}
}
