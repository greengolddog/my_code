#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 1'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll t;
	cin >> t;
	if (t < 1 || 100'000 < t) {
	       return 1;
      	}
	for (ll i = 0; i < t; i++) {
		ll l, r;
		cin >> l >> r;
		if (l < 1 || r < l || INF < r) {
			return 1;
		}
	}
	return 0;
}
