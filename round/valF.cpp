#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n;
	cin >> n;
	if (n < 1 || 1'000'000 < n) {
		return 1;
	}
	ll y = 0;
	for (ll i = 0; i < n; i++) {
		char c;
		cin >> c;
		if (c != '+' && c != '?') {
			return 1;
		}
		ll l, r, x;
		cin >> l >> r >> x;
		if (l < 0 || r < l || y < r) {
			return 1;
		}
		if (x < 1 || 1'000'000'000 < x) {
			return 1;
		}
		if (c == '+') {
			y += x;
		}
	}
	return 0;
}
