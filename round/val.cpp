#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll t;
	cin >> t;
	ll x = 0;
	for (ll i = 0; i < t; i++) {
		set<ll> s;
		ll n;
		cin >> n;
		x += n;
		if (n < 2 || 200'000 < n) {
			return 1;
		}
		for (ll j = 0; j < n; j++) {
			ll a;
			cin >> a;
			s.insert(a);
		}
		if (!(s.size() == n && *s.begin() == 1 && *s.rbegin() == n)) {
			return 1;
		}
	}
	if (x > 1'000'000) {
		return 1;
	}
	return 0;
}
