#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

struct node {
	node *p;
	ll x;
	node(node *p = nullptr, ll x = 0): p(p), x(x) {}
	node *add(ll c) {
		return new node(this, x + c);
	}
	node *del() {
		return p;
	}
};

int main() {
    	ios_base::sync_with_stdio(false);
    	cin.tie(0);
    	cout.tie(0);
	ll n;
	cin >> n;
	ll ans = 0;
	vector<node*> arr(1, new node(nullptr, 0));
	for (ll i = 0; i < n; i++) {
		ll t;
		cin >> t;
		ll x;
		cin >> x;
		if (x == 0) {
			arr.push_back(arr[t]->del());
		} else {
			arr.push_back(arr[t]->add(x));
		}
		ans += arr.back()->x;
	}
	cout << ans;
}
