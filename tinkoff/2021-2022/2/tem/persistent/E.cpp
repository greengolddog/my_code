#include <bits/stdc++.h>
typedef long long ll;

const ll INF = 9'000'000'000'000'000'000;

template<typename T>
bool assign_max(T& a, T b) {
	if (b > a) {
		a = b;
		return true;
	}
	return false;
}

template<typename T>
bool assign_min(T& a, T b) {
	if (b < a) {
		a = b;
		return true;
	}
	return false;
}

using namespace std;

struct node {
	ll x = INF;
	node *sl = nullptr, *sr = nullptr;
	ll l, r;
	node(node *n) {
		x = n->x;
		sl = n->sl;
		sr = n->sr;
		l = n->l;
		r = n->r;
	}
	node(ll l, ll r): l(l), r(r) {
		ll mid = (l + r) / 2;
		if (r - l > 1) {
			sl = new node(l, mid);
			sr = new node(mid, r);
		}
	}
	node *get_l() {
		return l = new node(l);
	}
	node *get_r() {
		return r = new node(r);
	}
	void change(ll num, ll n) {
		if (l > num || num >= r) {
			return;
		}
		if (r - l == 1) {
			x = n;
			return;
		}
		get_l()->change(num, n);
		get_r()->change(num, n);
		x = min(sl->x, sr->x);
	}
};

